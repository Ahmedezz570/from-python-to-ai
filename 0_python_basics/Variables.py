# Creating Variables
# Python has no command for declaring a variable.
# A variable is created the moment you first assign a value to it.
x = 5  # int
y = "John" # string(str)
print(x)
print(y)
# Casting
a = float(3000)
b = str(200)
print(type(a))
print(type(b))
# Single or Double Quotes?
x = "John"
# is the same as
x = 'John'
# Case-Sensitive
a = 4
A = "Sally"
#A will not overwrite a
# Variable Names
myvar = "John"
my_var = "John"
_my_var = "John"
myVar = "John"
MYVAR = "John"
myvar2 = "John"
# Camel Case
myVariableName = "John"
# Pascal Case
MyVariableName = "John"
# Snake Case
my_variable_name = "John"
# Many Values to Multiple Variables
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)
# One Value to Multiple Variables
x = y = z = "Orange"
print(x)
print(y)
print(z)
x = "Python"
y = "is"
z = "awesome"
print(x, y, z)
x = "Python "
y = "is "
z = "awesome"
print(x + y + z)
# Notice the space character after "Python " and "is ", without them the result would be "Pythonisawesome".

